@app.on_event("startup")
async def startup_whatsapp_readonly_auto_restore():
    """Auto-recover from the neonize 'attempt to write a readonly database'
    failure mode by hooking the whatsmeow.Client logger.

    History (4× over 30 hours, 2026-05-05 → 2026-05-06): the neonize
    SQLite session goes silently read-only after a few hours of uptime.
    Symptoms: incoming WhatsApp messages can't be decrypted/stored, no
    new chat_messages rows appear, the DB still shows status=connected.
    Manual fix is `docker compose restart api` which re-runs
    restore_connections() — neonize cleanly re-opens the session file.

    This hook attaches a logging Handler to the whatsmeow.Client logger.
    When a 'readonly database' record fires, it schedules a single
    restore_connections() call. Rate-limited to one trigger per 120s so
    a burst of decrypt failures doesn't cascade.
    """
    import asyncio
    import logging
    import time

    log = logging.getLogger(__name__)

    class _ReadonlyDetectingHandler(logging.Handler):
        def __init__(self, loop):
            super().__init__(level=logging.WARNING)
            self._last_trigger = 0.0
            self._loop = loop

        def emit(self, record):
            try:
                msg = record.getMessage()
            except Exception:
                return
            if "readonly database" not in msg:
                return
            now = time.monotonic()
            if now - self._last_trigger < 120.0:
                return  # cooldown — already recovering
            self._last_trigger = now
            log.warning(
                "WhatsApp readonly-DB pattern detected — auto-restoring "
                "connections (rate-limited: 120s cooldown)"
            )
            asyncio.run_coroutine_threadsafe(_do_restore(), self._loop)

    async def _do_restore():
        try:
            from app.services.whatsapp_service import whatsapp_service
            await whatsapp_service.restore_connections()
            log.info("WhatsApp auto-restore: completed successfully")
        except Exception as e:
            log.warning("WhatsApp auto-restore failed: %s", e)

    try:
        loop = asyncio.get_running_loop()
        handler = _ReadonlyDetectingHandler(loop)
        logging.getLogger("whatsmeow.Client").addHandler(handler)
        log.info("WhatsApp readonly-DB auto-restore handler installed")
    except Exception as e:
        log.warning("Failed to install WhatsApp auto-restore handler: %s", e)


@app.on_event("startup")
async def startup_teams_monitor_reconcile():
    """Re-spawn TeamsMonitorWorkflow for every enabled Teams account.

    Catches two scenarios:
      1. Tenants who enabled Teams before this workflow was deployed.
      2. API crashed between the channel-enable DB commit and the
         in-line ``start_workflow`` call in /teams/enable, leaving
         a "channel enabled, no monitor" zombie.

    Fire-and-forget via ``asyncio.create_task`` — the api becomes ready
    immediately and reconcile runs in the background. Was previously
    awaited inline; with 1000+ tenants the readiness probe would fail
    waiting on N sequential Temporal connects. PR-B follow-up to the
    2026-05-02 holistic review.

    Reconcile itself is bounded-concurrency (20 in flight) and uses
    a singleton Temporal client to avoid the per-call connect cost.
    """
    try:
        import asyncio
        from app.api.v1.channels import reconcile_teams_monitors

        async def _bg():
            try:
                await reconcile_teams_monitors()
            except Exception as e:
                import logging
                logging.getLogger(__name__).warning(
                    "Teams monitor reconcile (background) failed: %s", e,
                )

        # Fire-and-forget. Loop is the FastAPI startup loop; the task
        # runs alongside the request loop for the api lifetime.
        asyncio.create_task(_bg())
    except Exception as e:
        import logging
        logging.getLogger(__name__).warning(
            "Teams monitor reconcile skipped: %s", e,
        )


@app.on_event("startup")
async def startup_proactive_workflows():
    """Auto-start long-running Temporal workflows for all active tenants.

    Launches per-tenant dynamic workflow instances of:
      - Autonomous Learning (nightly dream/RL cycle + morning report)
      - Inbox Monitor (Gmail/Calendar monitoring every 15 min)
      - Competitor Monitor (competitor intelligence, daily)

    Uses workflow_id_reuse_policy to skip tenants that already have a
    running instance (idempotent on restart).
    """
    import asyncio
    import logging as _logging
    from app.db.session import SessionLocal as _SL

    logger = _logging.getLogger(__name__)

    async def _launch():
        # Give Temporal a few seconds to be ready
        await asyncio.sleep(5)
        try:
            from app.services.dynamic_workflow_launcher import start_dynamic_workflow_by_name

            db = _SL()
            try:
                from sqlalchemy import text
                tenants = db.execute(text(
                    "SELECT id::text FROM tenants"
                )).fetchall()
            finally:
                db.close()

            launched = 0
            for (tid,) in tenants:
                # Autonomous learning (nightly)
                try:
                    await start_dynamic_workflow_by_name("Autonomous Learning", tid)
                    launched += 1
                except Exception:
                    pass  # already running or template not found

                # Inbox monitor (every 15 min)
                try:
                    await start_dynamic_workflow_by_name("Inbox Monitor", tid)
                    launched += 1
                except Exception:
                    pass

                # Competitor monitor (daily)
                try:
                    await start_dynamic_workflow_by_name("Competitor Monitor", tid)
                    launched += 1
                except Exception:
                    pass

            logger.info("Proactive workflows: launched %d new instances for %d tenants", launched, len(tenants))
        except Exception as e:
            logger.warning("Proactive workflow auto-start failed (Temporal may not be ready): %s", e)

    asyncio.ensure_future(_launch())


