class LoggingMiddleware(BaseHTTPMiddleware):
    async def dispatch(self, request: Request, call_next):
        start_time = time.time()
        response = await call_next(request)
        duration_ms = (time.time() - start_time) * 1000
        
        log_request(
            logger,
            request.method,
            request.url.path,
            response.status_code,
            duration_ms,
            extra={
                "client_ip": request.client.host,
                "user_agent": request.headers.get("user-agent"),
            }
        )
        return response


class TrailingSlashMiddleware(BaseHTTPMiddleware):
    """Strip trailing slashes before routing to avoid 307 redirects that lose POST bodies."""
    async def dispatch(self, request: Request, call_next):
        if request.url.path != "/" and request.url.path.endswith("/"):
            scope = request.scope
            scope["path"] = scope["path"].rstrip("/")
            if scope.get("raw_path"):
                scope["raw_path"] = scope["raw_path"].rstrip(b"/")
        return await call_next(request)


class ForceHTTPSRedirectMiddleware(BaseHTTPMiddleware):
    """Ensure any redirect generated by the app uses HTTPS when behind a proxy."""
    async def dispatch(self, request: Request, call_next):
        response = await call_next(request)
        if response.status_code in (301, 302, 307, 308):
            location = response.headers.get("location", "")
            if location.startswith("http://") and request.headers.get("x-forwarded-proto") == "https":
                response.headers["location"] = location.replace("http://", "https://", 1)
        return response


